
const col1 = document.getElementById('myCol1');


col1.addEventListener('mouseenter', function() {
    col1.style.backgroundColor = 'rgb(96, 154, 51)';
    col1.style.color = 'white'; 
    const svgIcon = col1.querySelector('svg');
    svgIcon.style.fill = 'white'; 
    const paragraph = col1.querySelector('p');
    paragraph.style.color = 'white'; 
    const a = col1.querySelector('a');
    a.style.color = 'white'; 
});


col1.addEventListener('mouseleave', function() {
    col1.style.backgroundColor = 'rgba(255, 255, 255, 0.2)';
    col1.style.color = 'rgb(49, 42, 42)'; 
    const svgIcon = col1.querySelector('svg');
    svgIcon.style.fill = 'rgb(96, 154, 51)'; 
    const paragraph = col1.querySelector('p');
    paragraph.style.color = 'rgb(49, 42, 42)'; 
    const a = col1.querySelector('a');
    a.style.color = 'black'; 
});

